def load_data(path: str | Path) -> tuple[pd.DataFrame, pd.Series]:
    """Load the Nb-Si dataset CSV and return (X, y).

    Parameters
    ----------
    path:
        Path to the CSV file generated by ``data/generate_data.py``.

    Returns
    -------
    X : pd.DataFrame
        Feature matrix (all element columns).
    y : pd.Series
        Target vector (KIC column).
    """
    df = pd.read_csv(path)
    if "KIC" not in df.columns:
        raise ValueError(f"Expected 'KIC' column in {path}.")
    y = df["KIC"].copy()
    X = df.drop(columns=["KIC"])
    return X, y


def split_initial_pool(
    n_samples: int,
    n_initial: int,
    random_state: int = 0,
) -> list[int]:
    """Randomly select *n_initial* indices as the initial labelled pool.

    The remaining indices form the unlabelled candidate pool, which the
    :class:`~src.active_learning.ActiveLearner` computes internally by
    taking the complement of the labelled set.

    Parameters
    ----------
    n_samples:
        Total number of samples.
    n_initial:
        Number of samples to label initially.
    random_state:
        NumPy random seed.

    Returns
    -------
    initial_indices : list[int]
        Indices of the initially labelled samples.
    """
    rng = np.random.default_rng(random_state)
    all_idx = np.arange(n_samples)
    rng.shuffle(all_idx)
    return list(all_idx[:n_initial])


